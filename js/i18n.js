// ç¿»è¨³é–¢æ•°
function t(key, params = {}) {
  let text = translations[currentLanguage][key] || translations['en'][key] || key;
  
  // ãƒ‘ãƒ©ãƒ¡ãƒ¼ã‚¿ç½®æ›
  Object.keys(params).forEach(param => {
    text = text.replace(`{${param}}`, params[param]);
  });
  
  return text;
}

